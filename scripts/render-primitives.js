// Generates figures/primitives.static.html with the primitives SVG inlined,
// so it can be opened directly via file:// without a dev server.
// (figures/primitives.html uses ESM imports and needs HTTP.)

import { writeFileSync } from 'node:fs';
import {
  resistor,
  capacitor,
  varCapacitor,
  inductor,
  galvanometer,
  ground,
  switchSym,
  node,
  wire,
  vectorArrow,
  equationLine,
  COLOR,
} from '../shared/components.js';

const COLS = 4;
const ROWS = 3;
const W = 1100;
const H = 720;
const CELL_W = W / COLS;
const CELL_H = H / ROWS;

const items = [
  { label: 'RESISTOR',       render: (cx, cy) => resistor({ x: cx - 60, y: cy, length: 120 }) },
  { label: 'RESISTOR (V)',   render: (cx, cy) => resistor({ x: cx, y: cy - 60, length: 120, orientation: 'v' }) },
  { label: 'CAPACITOR',      render: (cx, cy) => capacitor({ x: cx - 36, y: cy }) },
  { label: 'VAR. CAPACITOR', render: (cx, cy) => varCapacitor({ x: cx - 36, y: cy }) },
  { label: 'INDUCTOR',       render: (cx, cy) => inductor({ x: cx - 60, y: cy, length: 120 }) },
  { label: 'GALVANOMETER',   render: (cx, cy) => galvanometer({ x: cx, y: cy }) },
  { label: 'GROUND',         render: (cx, cy) => wire([[cx, cy - 28], [cx, cy]]) + ground({ x: cx, y: cy }) },
  { label: 'SWITCH (OPEN)',  render: (cx, cy) => switchSym({ x: cx - 25, y: cy, length: 50 }) },
  { label: 'NODE',           render: (cx, cy) => node({ x: cx, y: cy, label: 'A' }) },
  { label: 'WIRE',           render: (cx, cy) => wire([[cx - 70, cy + 30], [cx - 70, cy - 30], [cx + 70, cy - 30], [cx + 70, cy + 30]]) },
  { label: 'VECTOR ARROW',   render: (cx, cy) => vectorArrow({ x1: cx - 40, y1: cy + 30, x2: cx + 40, y2: cy - 30, label: 'V' }) },
  { label: 'EQUATION',       render: (cx, cy) => equationLine({ x: cx, y: cy + 6, text: 'C_{12} = (R′/R″) C′', size: 18, align: 'middle' }) },
];

function buildGrid() {
  let body = '';
  items.forEach((item, idx) => {
    const col = idx % COLS;
    const row = Math.floor(idx / COLS);
    const cx = col * CELL_W + CELL_W / 2;
    const cy = row * CELL_H + CELL_H / 2 - 12;
    body += `<rect x="${col * CELL_W + 8}" y="${row * CELL_H + 8}" width="${CELL_W - 16}" height="${CELL_H - 16}" stroke="${COLOR}" stroke-width="0.5" fill="none" opacity="0.18"/>`;
    body += item.render(cx, cy);
    const ly = (row + 1) * CELL_H - 18;
    body += `<text x="${cx}" y="${ly}" font-size="11" font-family="'IBM Plex Mono', monospace" fill="${COLOR}" text-anchor="middle" letter-spacing="1">${item.label}</text>`;
  });
  return body;
}

const grid = buildGrid();

const html = `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8" />
<title>Primitives Test (static) — bell-labs-art</title>
<link rel="stylesheet" href="../shared/style.css" />
<style>
  .legend { font-size: 11px; opacity: 0.7; margin-bottom: 6px; letter-spacing: 0.05em; }
  .surround { outline: 1px dashed rgba(240, 238, 242, 0.18); }
  h1 { font-weight: 500; font-size: 13px; letter-spacing: 0.1em; text-transform: uppercase; margin: 0 0 4px 0; }
  header { max-width: 1100px; margin: 24px auto 0; padding: 0 40px; }
</style>
</head>
<body>
<header>
  <h1>Primitives test (static)</h1>
  <p class="legend">
    Generated by <code>node scripts/render-primitives.js</code>. Top SVG uses
    <code>shape-rendering="geometricPrecision"</code>; bottom uses
    <code>"crispEdges"</code>. Same content. The SVGs themselves have no
    background — the #444 you see is the page body.
  </p>
</header>
<div class="preview-stack">
  <section>
    <h2>geometricPrecision</h2>
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${W} ${H}" width="${W}" height="${H}" shape-rendering="geometricPrecision" class="surround" style="font-family:'IBM Plex Mono', monospace">${grid}</svg>
  </section>
  <section>
    <h2>crispEdges</h2>
    <svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${W} ${H}" width="${W}" height="${H}" shape-rendering="crispEdges" class="surround" style="font-family:'IBM Plex Mono', monospace">${grid}</svg>
  </section>
</div>
</body>
</html>
`;

const target = new URL('../figures/primitives.static.html', import.meta.url);
writeFileSync(target, html);
console.log('wrote', target.pathname);
